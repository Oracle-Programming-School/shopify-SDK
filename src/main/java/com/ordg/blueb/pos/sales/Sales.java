            invoice.put("FurtherTax", rs.getFloat("FurtherTax"));
            invoice.put("PaymentMode", rs.getInt("PaymentMode"));
            invoice.put("RefUSIN", rs.getString("RefUSIN"));
            invoice.put("InvoiceType", rs.getInt("InvoiceType"));
